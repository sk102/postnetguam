function SidebarContent({ onNavigate }: { onNavigate?: () => void }): React.ReactElement {
  const pathname = usePathname();

  return (
    <div className="flex h-full w-64 flex-col bg-postnet-charcoal">
      <div className="flex h-16 items-center justify-between px-6 border-b border-postnet-charcoal-light">
        <h1 className="text-xl font-bold text-white">
          <span className="text-postnet-red">Post</span>Net CMS
        </h1>
      </div>
      <nav className="flex-1 space-y-1 px-3 py-4">
        {navigation.map((item) => {
          const isActive = pathname === item.href ||
            (item.href !== '/' && pathname.startsWith(item.href));
          return (
            <Link
              key={item.name}
              href={item.href}
              {...(onNavigate && { onClick: onNavigate })}
              className={`flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium ${
                isActive
                  ? 'bg-postnet-red text-white'
                  : 'text-gray-300 hover:bg-postnet-charcoal-light hover:text-white'
              }`}
            >
              <item.icon className="h-5 w-5" />
              {item.name}
            </Link>
          );
        })}
      </nav>
    </div>
  );
}

export function Sidebar({ isOpen, onClose }: SidebarProps): React.ReactElement {
  return (
    <>
      {/* Desktop sidebar - always visible */}
      <div className="hidden lg:block">
        <SidebarContent />
      </div>

      {/* Mobile sidebar - overlay */}
      {isOpen && (
        <div className="fixed inset-0 z-50 lg:hidden">
          {/* Backdrop */}
          <div
            className="fixed inset-0 bg-black/50"
            onClick={onClose}
            aria-hidden="true"
          />

          {/* Sidebar panel */}
          <div className="fixed inset-y-0 left-0 flex w-64 flex-col">
            <div className="relative flex-1">
              <SidebarContent onNavigate={onClose} />

              {/* Close button */}
              <button
                onClick={onClose}
                className="absolute right-3 top-4 rounded-md p-1 text-gray-400 hover:bg-postnet-charcoal-light hover:text-white"
              >
                <CloseIcon className="h-6 w-6" />
              </button>
            </div>
          </div>
        </div>
      )}
    </>
  );
}
